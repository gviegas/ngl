                .descriptor_set = &self.sets[i],
                .binding = 0,
                .element = 0,
                .contents = .{ .uniform_buffer = ub[i * 2 .. i * 2 + 1] },
            };
            w[is.len + i * 2 + 1] = .{
                .descriptor_set = &self.sets[i],
                .binding = 1,
                .element = 0,
                .contents = .{ .uniform_buffer = ub[i * 2 + 1 .. i * 2 + 2] },
            };
            for (0..materials.len) |j| {
                const k = i * materials.len + j;
